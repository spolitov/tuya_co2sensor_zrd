static u8 ds_crc(const u8* input, u32 len) {
  u8 result = 0;
  for (const u8* end = input + len; input != end; ++input) {
    result = ds_crc8_table[result ^ *input];
  }
  return result;
}

static int ds1820_perform_read(void* arg) {
  if (!ow_reset()) {
    LOG_DS1820("Reset for read failure");
    return -1;
  }

  ow_write_byte(CMD_SKIP_ROM);
  ow_write_byte(CMD_READ_SCRATCHPAD);

  u8 scratchpad[9];
  for (int i = 0; i < 9; i++) {
    scratchpad[i] = ow_read_byte();
  }

  if (ds_crc(scratchpad, 8) != scratchpad[8]) {
    LOG_DS1820("Invalid CRC: %02x %02x %02x %02x %02x %02x %02x %02x %02x", scratchpad[0], scratchpad[1], scratchpad[2], scratchpad[3], scratchpad[4], scratchpad[5], scratchpad[6], scratchpad[7], scratchpad[8]);
    return -1;
  }

  s16 temp_raw = load_le16(scratchpad);
  zcl_temperature.measured_value = temp_raw * 6.25f;
  LOG_DS1820("Raw: %d, converted: %d", temp_raw, (s32)zcl_temperature.measured_value);
  return -1;
}

static void ds1820_read() {
  LOG_DS1820("read: %d", drv_gpio_read(PIN_DS1820));

  if (!ow_reset()) {
    LOG_DS1820("Reset for convert failure");
    return;
  }

  ow_write_byte(CMD_SKIP_ROM);
  ow_write_byte(CMD_CONVERT_TEMP);

  TL_ZB_TIMER_SCHEDULE(ds1820_perform_read, NULL, 750);
}

void ds1820_update() {
  if (!need_update(temp_report_cfg, 10, 2, &ds1820_measurement_time)) {
    return;
  }

  ds1820_read();
}

void ds1820_init() {
  drv_gpio_func_set(PIN_DS1820);
  drv_gpio_output_en(PIN_DS1820, 0);
  drv_gpio_input_en(PIN_DS1820, 1);

  temp_report_cfg = zcl_reportCfgInfoEntryFind(
      APP_ENDPOINT1, ZCL_CLUSTER_MS_TEMPERATURE_MEASUREMENT, ZCL_ATTRID_TEMPERATURE_MEASUREMENT_MEASUREDVALUE);
  if (!temp_report_cfg) {
    LOG_DS1820("Failed to find temp_report_cfg");
  }
}

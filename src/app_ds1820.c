#define LOG_DS1820(...) LOG_ON("DS1820", __VA_ARGS__)

const u32 PIN_DS1820 = GPIO_PC2;

static u32 ds1820_measurement_time = 0;
static reportCfgInfo_t* temp_report_cfg = NULL;

static const u8 CMD_SKIP_ROM = 0xcc;
static const u8 CMD_CONVERT_TEMP = 0x44;
static const u8 CMD_READ_SCRATCHPAD = 0xbe;

static void ow_pull() {
  drv_gpio_output_en(PIN_DS1820, 1);
  drv_gpio_write(PIN_DS1820, 0);
}

static void ow_release() {
  drv_gpio_output_en(PIN_DS1820, 0);
}

static bool ow_reset() {
  ow_pull();
  sleep_us(480);
  ow_release();
  sleep_us(60);
  bool result = !drv_gpio_read(PIN_DS1820);
  sleep_us(420);
  return result;
}

static void ow_write_bit(bool bit) {
  ow_pull();
  if (bit) {
    sleep_us(5);
    ow_release();
    sleep_us(60);
  } else {
    sleep_us(60);
    ow_release();
    sleep_us(5);
  }
}

static bool ow_read_bit() {
  ow_pull();
  sleep_us(1);
  ow_release();
  sleep_us(14);
  bool bit = drv_gpio_read(PIN_DS1820) != 0;
  sleep_us(45);
  return bit;
}

static void ow_write_byte(u8 byte) {
  for (int i = 0; i < 8; i++) {
    ow_write_bit(byte & 1);
    byte >>= 1;
  }
}

static u8 ow_read_byte() {
  u8 result = 0;
  for (int i = 0; i < 8; i++) {
    result |= ow_read_bit() << i;
  }
  return result;
}

